/* Helper: read a line from stdin, strip newline */
static void read_input(char *buf, int sz) {
    if (!fgets(buf, sz, stdin)) { buf[0] = 0; return; }
    int len = strlen(buf);
    while (len > 0 && (buf[len-1] == '\n' || buf[len-1] == '\r')) buf[--len] = 0;
}

/* Helper: prompt with a default value */
static void prompt_str(const char *question, const char *def, char *out, int sz) {
    if (def && def[0])
        printf("\033[1m%s\033[0m [\033[2m%s\033[0m]: ", question, def);
    else
        printf("\033[1m%s\033[0m: ", question);
    fflush(stdout);
    read_input(out, sz);
    if (!out[0] && def) strncpy(out, def, sz - 1);
}

/* Helper: yes/no prompt */
static int prompt_yn(const char *question, int def_yes) {
    printf("\033[1m%s\033[0m [\033[2m%s\033[0m]: ", question, def_yes ? "Y/n" : "y/N");
    fflush(stdout);
    char resp[16];
    read_input(resp, sizeof(resp));
    if (!resp[0]) return def_yes;
    return (resp[0] == 'y' || resp[0] == 'Y');
}

/* Helper: multi-select from a list of options. Returns bitmask. */
static int prompt_multi(const char *question, const char *options[], int count, char *selected_names, int name_sz) {
    printf("\033[1m%s\033[0m\n", question);
    printf("\033[2m  (comma-separated numbers, or 'all', or 'none')\033[0m\n");
    for (int i = 0; i < count; i++) {
        printf("  \033[36m%d\033[0m) %s\n", i + 1, options[i]);
    }
    printf("  \033[1mSelection:\033[0m ");
    fflush(stdout);

    char resp[256];
    read_input(resp, sizeof(resp));

    selected_names[0] = 0;
    int bitmask = 0;

    if (strcasecmp(resp, "all") == 0) {
        for (int i = 0; i < count; i++) {
            bitmask |= (1 << i);
            if (i > 0) strncat(selected_names, ",", name_sz - strlen(selected_names) - 1);
            strncat(selected_names, options[i], name_sz - strlen(selected_names) - 1);
        }
        return bitmask;
    }
    if (strcasecmp(resp, "none") == 0 || !resp[0]) {
        return 0;
    }

    /* Parse comma-separated numbers */
    char *saveptr = NULL;
    char *tok = strtok_r(resp, ",", &saveptr);
    while (tok) {
        /* trim spaces */
        while (*tok == ' ') tok++;
        int idx = atoi(tok) - 1;
        if (idx >= 0 && idx < count) {
            bitmask |= (1 << idx);
            if (selected_names[0]) strncat(selected_names, ",", name_sz - strlen(selected_names) - 1);
            strncat(selected_names, options[idx], name_sz - strlen(selected_names) - 1);
        }
        tok = strtok_r(NULL, ",", &saveptr);
    }
    return bitmask;
}

/* Helper: single select from a list */
static int prompt_select(const char *question, const char *options[], int count, int def_idx) {
    printf("\033[1m%s\033[0m\n", question);
    for (int i = 0; i < count; i++) {
        printf("  \033[36m%d\033[0m) %s%s\n", i + 1, options[i],
               (i == def_idx) ? " \033[2m(default)\033[0m" : "");
    }
    printf("  \033[1mChoice:\033[0m ");
    fflush(stdout);

    char resp[16];
    read_input(resp, sizeof(resp));
    if (!resp[0]) return def_idx;
    int idx = atoi(resp) - 1;
    if (idx < 0 || idx >= count) return def_idx;
    return idx;
}

