int cmd_search(int argc, char **argv) {
    if (argc < 2) {
        fprintf(stderr, "Usage: devindocs search <query> [--section <name>] [--json] [--limit N]\n");
        fprintf(stderr, "  --section <name>  Search only within a specific section\n");
        fprintf(stderr, "  --json            Output results as JSON\n");
        fprintf(stderr, "  --limit N         Limit results to N (default: 50)\n");
        fprintf(stderr, "Example: devindocs search \"cascade hooks\"\n");
        fprintf(stderr, "         devindocs search \"hooks\" --section cli --limit 5\n");
        return 1;
    }

    /* Parse flags and build query */
    char query[1024] = "";
    char section_filter[128] = "";
    int json_output = 0;
    int limit = 50;

    for (int i = 1; i < argc; i++) {
        if (strcmp(argv[i], "--section") == 0 || strcmp(argv[i], "-s") == 0) {
            if (i + 1 < argc) {
                strncpy(section_filter, argv[++i], sizeof(section_filter) - 1);
                section_filter[sizeof(section_filter)-1] = 0;
            }
        } else if (strcmp(argv[i], "--json") == 0) {
            json_output = 1;
        } else if (strcmp(argv[i], "--limit") == 0 || strcmp(argv[i], "-l") == 0) {
            if (i + 1 < argc) limit = atoi(argv[++i]);
            if (limit < 1) limit = 50;
        } else if (strcmp(argv[i], "--help") == 0 || strcmp(argv[i], "-h") == 0) {
            fprintf(stderr, "Usage: devindocs search <query> [--section <name>] [--json] [--limit N]\n");
            return 0;
        } else {
            if (query[0]) strcat(query, " ");
            strncat(query, argv[i], sizeof(query) - strlen(query) - 1);
        }
    }

    if (!query[0]) {
        fprintf(stderr, "Error: no search query provided.\n");
        return 1;
    }

    /* Convert query to lowercase for case-insensitive search */
    char qlower[1024];
    strncpy(qlower, query, sizeof(qlower) - 1);
    qlower[sizeof(qlower)-1] = 0;
    for (char *p = qlower; *p; p++) *p = tolower(*p);

    sqlite3 *db = db_open();
    sqlite3_stmt *st;

    /* Split query into words and build OR-based LIKE search */
    char words[16][128];
    int word_count = 0;
    char *saveptr = NULL;
    char qcopy[1024];
    strncpy(qcopy, qlower, sizeof(qcopy) - 1);
    qcopy[sizeof(qcopy)-1] = 0;
    char *tok = strtok_r(qcopy, " ", &saveptr);
    while (tok && word_count < 16) {
        strncpy(words[word_count], tok, 127);
        words[word_count][127] = 0;
        word_count++;
        tok = strtok_r(NULL, " ", &saveptr);
    }

    /* Build SQL with OR conditions for each word */
    char sql[4096];
    snprintf(sql, sizeof(sql),
        "SELECT d.section, d.name, "
        "  substr(d.content, max(1, instr(si.content_lower, ?) - 50), 200) as snippet, "
        "  (");
    for (int i = 0; i < word_count; i++) {
        if (i > 0) strcat(sql, " + ");
        strcat(sql, "CASE WHEN si.content_lower LIKE ? THEN 1 ELSE 0 END");
    }
    strcat(sql, ") as score ");
    strcat(sql, "FROM docs d JOIN search_index si ON si.doc_id = d.id ");
    strcat(sql, "WHERE ");
    if (section_filter[0]) {
        strcat(sql, "d.section = ? AND (");
    }
    for (int i = 0; i < word_count; i++) {
        if (i > 0) strcat(sql, " OR ");
        strcat(sql, "si.content_lower LIKE ?");
    }
    if (section_filter[0]) strcat(sql, ")");
    strcat(sql, " ORDER BY score DESC, d.section, d.name");
    if (limit > 0) {
        char lim_str[32];
        snprintf(lim_str, sizeof(lim_str), " LIMIT %d", limit);
        strcat(sql, lim_str);
    }

    sqlite3_prepare_v2(db, sql, -1, &st, NULL);

    /* Bind parameters — order must match the SQL ? placeholders */
    /* SQL: SELECT ... instr(si.content_lower, ?) ... WHERE [d.section = ? AND] (LIKE ? OR ...) (LIKE ? OR ...) */
    int bind_idx = 1;
    sqlite3_bind_text(st, bind_idx++, qlower, -1, SQLITE_STATIC);  /* for instr */

    /* Bind LIKE params for score (these come before WHERE clause in SQL) */
    char like_q[256];
    for (int i = 0; i < word_count; i++) {
        snprintf(like_q, sizeof(like_q), "%%%s%%", words[i]);
        sqlite3_bind_text(st, bind_idx++, like_q, -1, SQLITE_TRANSIENT);
    }

    /* Bind section filter (if present, comes before LIKE in WHERE clause) */
    if (section_filter[0]) {
        sqlite3_bind_text(st, bind_idx++, section_filter, -1, SQLITE_STATIC);
    }

    /* Bind LIKE params for WHERE */
    for (int i = 0; i < word_count; i++) {
        snprintf(like_q, sizeof(like_q), "%%%s%%", words[i]);
        sqlite3_bind_text(st, bind_idx++, like_q, -1, SQLITE_TRANSIENT);
    }

    int found = 0;
    if (json_output) {
        printf("[");
    } else {
        printf("\033[1mSearch results for: '%s'\033[0m", query);
        if (section_filter[0]) printf(" \033[2m(in section: %s)\033[0m", section_filter);
        printf("\n\n");
    }

    while (sqlite3_step(st) == SQLITE_ROW) {
        found++;
        const char *section = (const char*)sqlite3_column_text(st, 0);
        const char *name = (const char*)sqlite3_column_text(st, 1);
        const char *snippet = (const char*)sqlite3_column_text(st, 2);
        int score = sqlite3_column_int(st, 3);

        if (json_output) {
            if (found > 1) printf(",");
            printf("\n  {\"section\":\"%s\",\"name\":\"%s\",\"score\":%d}", section, name, score);
        } else {
            printf("  \033[1;36m%s/%s\033[0m %s\n", section, name, score > 1 ? "\033[1;32m★\033[0m" : "");
            if (snippet && snippet[0]) {
                char clean[250];
                strncpy(clean, snippet, sizeof(clean) - 1);
                clean[sizeof(clean)-1] = 0;
                for (char *p = clean; *p; p++) if (*p == '\n' || *p == '\r') *p = ' ';
                utf8_truncate(clean, 180);
                printf("  \033[2m%s...\033[0m\n\n", clean);
            } else {
                printf("\n");
            }
        }
    }
    sqlite3_finalize(st);

    if (json_output) {
        printf("%s]\n", found > 0 ? "\n" : "");
    } else {
        if (!found) {
            printf("\033[2mNo results found. Try a different query or run 'devindocs refresh' to update.\033[0m\n");
        } else {
            printf("\033[2m%d result(s)%s. Use 'devindocs show <section>' to see full content.\033[0m\n",
                   found, (limit > 0 && found >= limit) ? " (limit reached)" : "");
        }
    }

    sqlite3_close(db);
    return 0;
}

