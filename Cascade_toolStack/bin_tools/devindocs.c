int main(int argc, char **argv) {
    if (argc < 2) {
        print_help();
        return 0;
    }

    /* Parse global flags: --db <path> can appear before or after the command */
    int cmd_start = 1;
    for (int i = 1; i < argc; i++) {
        if (strcmp(argv[i], "--db") == 0 && i + 1 < argc) {
            strncpy(g_db_path, argv[i + 1], sizeof(g_db_path) - 1);
            g_db_path[sizeof(g_db_path)-1] = 0;
            /* Remove these two args from the array by shifting */
            for (int j = i; j < argc - 2; j++) {
                argv[j] = argv[j + 2];
            }
            argc -= 2;
            i--;  /* re-check this position */
        }
    }

    if (argc < 2) {
        print_help();
        return 0;
    }

    /* Resolve command */
    const char *cmd = argv[1];
    for (int i = 0; g_bindings[i].name; i++) {
        if (strcasecmp(cmd, g_bindings[i].name) == 0) {
            return g_bindings[i].fn(argc - 1, argv + 1);
        }
    }

    /* Check for -h / --help / help */
    if (strcasecmp(cmd, "-h") == 0 || strcasecmp(cmd, "--help") == 0 || strcasecmp(cmd, "help") == 0) {
        print_help();
        return 0;
    }

    fprintf(stderr, "Unknown command: %s\n", cmd);
    fprintf(stderr, "Run 'devindocs help' for usage.\n");
    return 1;
}
