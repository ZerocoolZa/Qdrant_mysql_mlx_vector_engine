/*
 * devindocs.c — Self-contained Devin Documentation CLI
 * Version: 1.0
 * Author:  wws / Cascade
 * License: Private
 *
 * ============================================================================
 * PURPOSE
 * ============================================================================
 *   Single-binary CLI that embeds all Devin documentation (Cascade, CLI,
 *   context, models, guidelines) in an in-memory SQLite database. Provides
 *   full-text search, doc listing, file generation (rules, skills, workflows,
 *   hooks), and a built-in web scraper to refresh from docs.devin.ai.
 *
 *   The binary is fully self-contained — no external .db file needed for
 *   first run. All docs live in compiled-in seed data. A persisted DB at
 *   ~/.devindocs.db stores refreshed content from the web scraper.
 *
 * ============================================================================
 * ARCHITECTURE
 * ============================================================================
 *   1. Command Binding Table (g_bindings[])
 *      - Maps command names (search, list, show, gen, refresh, etc.) to C functions
 *
 *   2. SQLite In-Memory DB
 *      - SEED_SQL creates tables: docs, templates, config
 *      - Seed data from devindocs_seed.h (auto-generated by build script)
 *      - db_open() seeds from compiled-in data, then merges persisted updates
 *
 *   3. Web Scraper (cmd_refresh)
 *      - Embedded Python script (SCRAPER_LINES[]) fetched via popen()
 *      - Fetches https://docs.devin.ai/llms.txt, then each page
 *      - Writes updated content to ~/.devindocs.db
 *
 *   4. File Generator (cmd_gen)
 *      - Templates for rules, skills, workflows, hooks
 *      - Writes to .devin/ directory structure
 *
 *   5. Embedded PyQt6 GUI
 *      - GUI_LINES[] contains a full Python script as C string literals
 *      - `devindocs -cfg` writes it to a temp file and launches python3
 *
 * ============================================================================
 * COMMANDS
 * ============================================================================
 *   devindocs search <query>       Full-text search across all docs
 *   devindocs list                 List all doc sections
 *   devindocs show <section>       Show a specific doc section
 *   devindocs sections             List available sections
 *   devindocs gen rule <name>      Generate a rule file
 *   devindocs gen skill <name>     Generate a SKILL.md file
 *   devindocs gen workflow <name>  Generate a workflow file
 *   devindocs gen hook <name>      Generate a hooks.v1.json snippet
 *   devindocs gen agents <name>    Generate an AGENT.md subagent profile
 *   devindocs templates            List available templates
 *   devindocs refresh              Re-fetch docs from docs.devin.ai
 *   devindocs refresh --check      Check if updates are available
 *   devindocs info                 Show build info and doc counts
 *   devindocs -cfg                 Launch PyQt6 config GUI
 *
 * ============================================================================
 * BUILD
 * ============================================================================
 *   ./devindocs_build.sh
 *   (Generates devindocs_seed.h from markdown files, then compiles)
 *
 * ============================================================================
 * USAGE
 * ============================================================================
 *   devindocs search "cascade hooks"
 *   devindocs show cli-reference
 *   devindocs gen skill review
 *   devindocs refresh
 *   devindocs -cfg
 *
 * ============================================================================
 */

