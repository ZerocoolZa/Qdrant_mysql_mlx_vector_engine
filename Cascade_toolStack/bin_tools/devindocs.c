/* ════════════════════════════════════════════
 * COMMAND: info
 * ════════════════════════════════════════════ */

int cmd_info(int argc, char **argv) {
    (void)argc; (void)argv;
    sqlite3 *db = db_open();
    sqlite3_stmt *st;

    printf("\033[1mdevindocs v1.0\033[0m — Self-contained Devin Documentation CLI\n\n");

    /* Doc counts */
    sqlite3_prepare_v2(db, "SELECT count(*), sum(length(content)) FROM docs", -1, &st, NULL);
    if (sqlite3_step(st) == SQLITE_ROW) {
        int cnt = sqlite3_column_int(st, 0);
        int total = sqlite3_column_int(st, 1);
        printf("  Docs:       %d sections, %d bytes total\n", cnt, total);
    }
    sqlite3_finalize(st);

    /* Template counts */
    sqlite3_prepare_v2(db, "SELECT count(*) FROM templates", -1, &st, NULL);
    if (sqlite3_step(st) == SQLITE_ROW) {
        printf("  Templates:  %d available\n", sqlite3_column_int(st, 0));
    }
    sqlite3_finalize(st);

    /* Config */
    char last[256], url[512], interval[32];
    db_get_config(db, "last_refresh", "", last, sizeof(last));
    db_get_config(db, "refresh_url", "https://docs.devin.ai/llms.txt", url, sizeof(url));
    db_get_config(db, "refresh_interval_days", "7", interval, sizeof(interval));
    printf("  Last refresh:  %s\n", last[0] ? last : "never (using compiled-in docs)");
    printf("  Refresh interval: %s days\n", interval);
    printf("  Source URL:    %s\n", url);
    printf("  Persisted DB:  %s\n", g_db_path);

    printf("\n\033[2mCommands: search, list, show, sections, gen, templates, init, export, reset, update, doctor, cron, refresh, info, -cfg\033[0m\n");

    sqlite3_close(db);
    return 0;
}

