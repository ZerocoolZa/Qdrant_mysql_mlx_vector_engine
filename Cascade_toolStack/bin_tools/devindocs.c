/* Signal handler for cmd_cfg — clean up lock on SIGTERM/SIGINT */
static void cfg_signal_handler(int sig) {
    (void)sig;
    lock_release(g_gui_lock);
    _exit(130);  /* 128 + SIGINT (2) */
}

int cmd_cfg(int argc, char **argv) {
    (void)argc; (void)argv;
    get_home_dir();

    /* Acquire GUI lock — prevent multiple GUI instances */
    int lock_rc = lock_acquire(g_gui_lock);
    if (lock_rc == 0) {
        int holder = lock_holder(g_gui_lock);
        fprintf(stderr, "\033[1;33mGUI is already running (PID %d).\033[0m\n", holder);
        fprintf(stderr, "Close the existing window first, or run 'devindocs reset --force' to clear the lock.\n");
        return 1;
    } else if (lock_rc < 0) {
        fprintf(stderr, "Warning: cannot create lock file %s — proceeding anyway.\n", g_gui_lock);
    }

    /* Install signal handlers to clean up lock on kill */
    signal(SIGTERM, cfg_signal_handler);
    signal(SIGINT, cfg_signal_handler);
    signal(SIGHUP, cfg_signal_handler);

    /* Check if refresh is in progress — warn but don't block */
    int refresh_pid = lock_holder(g_refresh_lock);
    if (refresh_pid > 0) {
        printf("\033[1;33mNote: Refresh is in progress (PID %d). GUI will show existing data.\033[0m\n", refresh_pid);
    }

    /* Write GUI script to temp file and launch */
    char tmp_path[512];
    snprintf(tmp_path, sizeof(tmp_path), "/tmp/devindocs_gui_%d.py", getpid());

    FILE *f = fopen(tmp_path, "w");
    if (!f) {
        fprintf(stderr, "Error: cannot write GUI script\n");
        lock_release(g_gui_lock);
        return 1;
    }
    for (int i = 0; GUI_LINES[i]; i++) {
        fputs(GUI_LINES[i], f);
    }
    fclose(f);

    char cmd[1024];
    snprintf(cmd, sizeof(cmd), "python3 %s %s", tmp_path, g_db_path);
    int rc = system(cmd);

    /* Cleanup */
    unlink(tmp_path);
    lock_release(g_gui_lock);

    /* Restore default signal handlers */
    signal(SIGTERM, SIG_DFL);
    signal(SIGINT, SIG_DFL);
    signal(SIGHUP, SIG_DFL);

    return rc;
}

