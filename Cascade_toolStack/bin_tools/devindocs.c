/* Check if a PID is alive */
static int pid_alive(int pid) {
    if (pid <= 0) return 0;
    return kill(pid, 0) == 0;
}

/* Try to acquire a lock file. Returns PID if locked by us, 0 if locked by
 * someone else, -1 on error. If the existing lock is stale (process dead),
 * it is removed and we acquire it. */
static int lock_acquire(const char *lock_path) {
    FILE *f = fopen(lock_path, "r");
    if (f) {
        int old_pid = 0;
        if (fscanf(f, "%d", &old_pid) == 1 && pid_alive(old_pid)) {
            fclose(f);
            return 0;  /* locked by someone else */
        }
        fclose(f);
        unlink(lock_path);  /* stale lock — remove */
    }

    f = fopen(lock_path, "w");
    if (!f) return -1;
    fprintf(f, "%d\n", getpid());
    fclose(f);
    return getpid();
}

/* Release a lock file (only if we own it) */
static void lock_release(const char *lock_path) {
    FILE *f = fopen(lock_path, "r");
    if (!f) return;
    int pid = 0;
    if (fscanf(f, "%d", &pid) == 1 && pid == getpid()) {
        fclose(f);
        unlink(lock_path);
    } else {
        fclose(f);
    }
}

/* Check who holds the lock (returns PID, or 0 if free) */
static int lock_holder(const char *lock_path) {
    FILE *f = fopen(lock_path, "r");
    if (!f) return 0;
    int pid = 0;
    if (fscanf(f, "%d", &pid) == 1 && pid_alive(pid)) {
        fclose(f);
        return pid;
    }
    fclose(f);
    return 0;
}

