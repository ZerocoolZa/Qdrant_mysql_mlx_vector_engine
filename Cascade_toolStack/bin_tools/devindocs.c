static void get_home_dir(void) {
    if (g_home[0]) return;
    const char *h = getenv("HOME");
    if (!h) {
        struct passwd *pw = getpwuid(getuid());
        h = pw ? pw->pw_dir : "/tmp";
    }
    strncpy(g_home, h, sizeof(g_home) - 1);
    g_home[sizeof(g_home)-1] = 0;
    /* Only set default DB path if not already set by --db flag */
    if (!g_db_path[0]) {
        snprintf(g_db_path, sizeof(g_db_path), "%s/.devindocs.db", g_home);
    }
    /* Lock file paths */
    snprintf(g_gui_lock, sizeof(g_gui_lock), "%s/.devindocs.gui.lock", g_home);
    snprintf(g_refresh_lock, sizeof(g_refresh_lock), "%s/.devindocs.refresh.lock", g_home);
}

/* UTF-8 safe truncation: backs up to a valid UTF-8 character boundary.
 * UTF-8 continuation bytes are 10xxxxxx (0x80-0xBF).
 * A leading byte is 0xxxxxxx, 110xxxxx, 1110xxxx, or 11110xxx. */
static void utf8_truncate(char *str, int max_bytes) {
    int len = strlen(str);
    if (len <= max_bytes) return;
    str[max_bytes] = 0;
    /* Back up until we're at a valid start byte (not a continuation byte) */
    while (max_bytes > 0) {
        unsigned char c = str[max_bytes - 1];
        if ((c & 0xC0) != 0x80) break;  /* Not a continuation byte — valid boundary */
        str[--max_bytes] = 0;
    }
}

static sqlite3 *db_open(void) {
    sqlite3 *db = NULL;
    sqlite3_open(":memory:", &db);
    if (!db) return NULL;

    /* Create schema */
    sqlite3_exec(db, SEED_SQL, NULL, NULL, NULL);

    /* Seed docs from compiled-in data */
    sqlite3_stmt *st;
    sqlite3_prepare_v2(db,
        "INSERT OR IGNORE INTO docs (section, name, content, fetched_at) VALUES (?,?,?,?)",
        -1, &st, NULL);

    char timestamp[32];
    time_t now = time(NULL);
    strftime(timestamp, sizeof(timestamp), "%Y-%m-%d", gmtime(&now));

    for (int i = 0; DOC_SEED[i].content; i++) {
        sqlite3_bind_text(st, 1, DOC_SEED[i].section, -1, SQLITE_STATIC);
        sqlite3_bind_text(st, 2, DOC_SEED[i].name, -1, SQLITE_STATIC);
        sqlite3_bind_text(st, 3, DOC_SEED[i].content, -1, SQLITE_STATIC);
        sqlite3_bind_text(st, 4, timestamp, -1, SQLITE_STATIC);
        sqlite3_step(st);
        sqlite3_reset(st);
    }
    sqlite3_finalize(st);

    /* Seed templates */
    sqlite3_prepare_v2(db,
        "INSERT OR IGNORE INTO templates (type, name, description, content) VALUES (?,?,?,?)",
        -1, &st, NULL);
    for (int i = 0; TPL_SEED[i].content; i++) {
        sqlite3_bind_text(st, 1, TPL_SEED[i].type, -1, SQLITE_STATIC);
        sqlite3_bind_text(st, 2, TPL_SEED[i].name, -1, SQLITE_STATIC);
        sqlite3_bind_text(st, 3, TPL_SEED[i].desc, -1, SQLITE_STATIC);
        sqlite3_bind_text(st, 4, TPL_SEED[i].content, -1, SQLITE_STATIC);
        sqlite3_step(st);
        sqlite3_reset(st);
    }
    sqlite3_finalize(st);

    /* Seed config */
    sqlite3_exec(db,
        "INSERT OR IGNORE INTO config (key,value,description) VALUES "
        "('refresh_url','https://docs.devin.ai/llms.txt','URL for doc index'),"
        "('refresh_interval_days','7','Auto-refresh interval in days'),"
        "('last_refresh','','Last successful refresh timestamp'),"
        "('auto_refresh','0','Auto-refresh on startup if stale');",
        NULL, NULL, NULL);

    /* Build search index (lowercase content for LIKE search) */
    sqlite3_exec(db,
        "INSERT OR REPLACE INTO search_index (doc_id, section, name, content_lower) "
        "SELECT id, section, name, lower(content) FROM docs;",
        NULL, NULL, NULL);

    /* Merge persisted updates from ~/.devindocs.db */
    get_home_dir();
    sqlite3 *pdb = NULL;
    if (sqlite3_open(g_db_path, &pdb) == SQLITE_OK && pdb) {
        /* Check if persisted DB has required tables */
        sqlite3_stmt *chk;
        int has_docs = 0, has_config = 0;
        if (sqlite3_prepare_v2(pdb,
            "SELECT count(*) FROM sqlite_master WHERE type='table' AND name='docs'",
            -1, &chk, NULL) == SQLITE_OK) {
            if (sqlite3_step(chk) == SQLITE_ROW) has_docs = sqlite3_column_int(chk, 0) > 0;
            sqlite3_finalize(chk);
        }
        if (sqlite3_prepare_v2(pdb,
            "SELECT count(*) FROM sqlite_master WHERE type='table' AND name='config'",
            -1, &chk, NULL) == SQLITE_OK) {
            if (sqlite3_step(chk) == SQLITE_ROW) has_config = sqlite3_column_int(chk, 0) > 0;
            sqlite3_finalize(chk);
        }

        /* Only merge if persisted DB has the right tables */
        if (has_docs) {
            sqlite3_stmt *pst;
            if (sqlite3_prepare_v2(pdb,
                "SELECT section, name, content, fetched_at FROM docs", -1, &pst, NULL) == SQLITE_OK) {
                sqlite3_stmt *up;
                sqlite3_prepare_v2(db,
                    "INSERT OR REPLACE INTO docs (section, name, content, fetched_at) VALUES (?,?,?,?)",
                    -1, &up, NULL);
                while (sqlite3_step(pst) == SQLITE_ROW) {
                    sqlite3_bind_text(up, 1, (const char*)sqlite3_column_text(pst, 0), -1, SQLITE_STATIC);
                    sqlite3_bind_text(up, 2, (const char*)sqlite3_column_text(pst, 1), -1, SQLITE_STATIC);
                    sqlite3_bind_text(up, 3, (const char*)sqlite3_column_text(pst, 2), -1, SQLITE_STATIC);
                    sqlite3_bind_text(up, 4, (const char*)sqlite3_column_text(pst, 3), -1, SQLITE_STATIC);
                    sqlite3_step(up);
                    sqlite3_reset(up);
                }
                sqlite3_finalize(up);
                sqlite3_finalize(pst);
            }
        }

        if (has_config) {
            sqlite3_stmt *pst;
            if (sqlite3_prepare_v2(pdb, "SELECT key, value FROM config", -1, &pst, NULL) == SQLITE_OK) {
                sqlite3_stmt *cup;
                sqlite3_prepare_v2(db,
                    "INSERT OR REPLACE INTO config (key, value, description) VALUES (?,?,?)",
                    -1, &cup, NULL);
                while (sqlite3_step(pst) == SQLITE_ROW) {
                    sqlite3_bind_text(cup, 1, (const char*)sqlite3_column_text(pst, 0), -1, SQLITE_STATIC);
                    sqlite3_bind_text(cup, 2, (const char*)sqlite3_column_text(pst, 1), -1, SQLITE_STATIC);
                    sqlite3_bind_text(cup, 3, "from persisted", -1, SQLITE_STATIC);
                    sqlite3_step(cup);
                    sqlite3_reset(cup);
                }
                sqlite3_finalize(cup);
                sqlite3_finalize(pst);
            }
        }

        /* Also merge templates from persisted DB if they exist */
        int has_templates = 0;
        if (sqlite3_prepare_v2(pdb,
            "SELECT count(*) FROM sqlite_master WHERE type='table' AND name='templates'",
            -1, &chk, NULL) == SQLITE_OK) {
            if (sqlite3_step(chk) == SQLITE_ROW) has_templates = sqlite3_column_int(chk, 0) > 0;
            sqlite3_finalize(chk);
        }
        if (has_templates) {
            sqlite3_stmt *pst;
            if (sqlite3_prepare_v2(pdb,
                "SELECT type, name, description, content FROM templates", -1, &pst, NULL) == SQLITE_OK) {
                sqlite3_stmt *tup;
                sqlite3_prepare_v2(db,
                    "INSERT OR REPLACE INTO templates (type, name, description, content) VALUES (?,?,?,?)",
                    -1, &tup, NULL);
                while (sqlite3_step(pst) == SQLITE_ROW) {
                    sqlite3_bind_text(tup, 1, (const char*)sqlite3_column_text(pst, 0), -1, SQLITE_STATIC);
                    sqlite3_bind_text(tup, 2, (const char*)sqlite3_column_text(pst, 1), -1, SQLITE_STATIC);
                    sqlite3_bind_text(tup, 3, (const char*)sqlite3_column_text(pst, 2), -1, SQLITE_STATIC);
                    sqlite3_bind_text(tup, 4, (const char*)sqlite3_column_text(pst, 3), -1, SQLITE_STATIC);
                    sqlite3_step(tup);
                    sqlite3_reset(tup);
                }
                sqlite3_finalize(tup);
                sqlite3_finalize(pst);
            }
        }

        sqlite3_close(pdb);
    }

    /* Rebuild search index after merge */
    sqlite3_exec(db, "DELETE FROM search_index;", NULL, NULL, NULL);
    sqlite3_exec(db,
        "INSERT INTO search_index (doc_id, section, name, content_lower) "
        "SELECT id, section, name, lower(content) FROM docs;",
        NULL, NULL, NULL);

    return db;
}

static void db_get_config(sqlite3 *db, const char *key, const char *def, char *out, int out_sz) {
    sqlite3_stmt *st;
    sqlite3_prepare_v2(db, "SELECT value FROM config WHERE key=?", -1, &st, NULL);
    sqlite3_bind_text(st, 1, key, -1, SQLITE_STATIC);
    if (sqlite3_step(st) == SQLITE_ROW) {
        const char *v = (const char*)sqlite3_column_text(st, 0);
        if (v) { strncpy(out, v, out_sz - 1); out[out_sz-1] = 0; sqlite3_finalize(st); return; }
    }
    sqlite3_finalize(st);
    strncpy(out, def, out_sz - 1);
    out[out_sz-1] = 0;
}

