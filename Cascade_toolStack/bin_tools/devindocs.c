/* Signal handler for cmd_refresh — clean up lock on kill */
static void refresh_signal_handler(int sig) {
    (void)sig;
    lock_release(g_refresh_lock);
    _exit(130);
}

int cmd_refresh(int argc, char **argv) {
    int check_only = (argc >= 2 && strcmp(argv[1], "--check") == 0);
    (void)argc; (void)argv;

    get_home_dir();

    /* Check if refresh needed */
    sqlite3 *db = db_open();
    char last[256], interval_str[32];
    db_get_config(db, "last_refresh", "", last, sizeof(last));
    db_get_config(db, "refresh_interval_days", "7", interval_str, sizeof(interval_str));
    int interval = atoi(interval_str);
    sqlite3_close(db);

    if (last[0] && strcmp(last, "never") != 0) {
        printf("Last refresh: %s (interval: %d days)\n", last, interval);
    } else {
        printf("No previous refresh. Will fetch all docs from docs.devin.ai.\n");
    }

    if (check_only) {
        if (!last[0] || strcmp(last, "never") == 0) {
            printf("Status: never refreshed — run 'devindocs refresh' to fetch.\n");
        } else {
            printf("Status: last refreshed on %s\n", last);
            printf("Run 'devindocs refresh' to update.\n");
        }
        return 0;
    }

    /* Acquire refresh lock — prevent concurrent refreshes */
    int lock_rc = lock_acquire(g_refresh_lock);
    if (lock_rc == 0) {
        int holder = lock_holder(g_refresh_lock);
        fprintf(stderr, "\033[1;33mRefresh already in progress (PID %d).\033[0m\n", holder);
        fprintf(stderr, "Wait for it to finish, or run 'devindocs reset --force' to clear the lock.\n");
        return 1;
    } else if (lock_rc < 0) {
        fprintf(stderr, "Warning: cannot create lock file %s — proceeding anyway.\n", g_refresh_lock);
    }

    /* Install signal handlers to clean up lock on kill */
    signal(SIGTERM, refresh_signal_handler);
    signal(SIGINT, refresh_signal_handler);
    signal(SIGHUP, refresh_signal_handler);

    /* Check if GUI is running — warn but don't block */
    int gui_pid = lock_holder(g_gui_lock);
    if (gui_pid > 0) {
        printf("\033[1;33mNote: GUI is running (PID %d). Data will reload on next GUI restart.\033[0m\n", gui_pid);
    }

    /* Write the scraper script to a temp file and run it */
    char tmp_path[512];
    snprintf(tmp_path, sizeof(tmp_path), "/tmp/devindocs_scraper_%d.py", getpid());

    FILE *f = fopen(tmp_path, "w");
    if (!f) {
        fprintf(stderr, "Error: cannot write scraper script to %s\n", tmp_path);
        lock_release(g_refresh_lock);
        return 1;
    }
    for (int i = 0; SCRAPER_LINES[i]; i++) {
        fputs(SCRAPER_LINES[i], f);
    }
    fclose(f);

    printf("Fetching docs from docs.devin.ai...\n\n");

    char cmd[1024];
    snprintf(cmd, sizeof(cmd), "python3 %s %s", tmp_path, g_db_path);
    int rc = system(cmd);

    /* Cleanup */
    unlink(tmp_path);
    lock_release(g_refresh_lock);

    /* Restore default signal handlers */
    signal(SIGTERM, SIG_DFL);
    signal(SIGINT, SIG_DFL);
    signal(SIGHUP, SIG_DFL);

    if (rc == 0) {
        printf("\n\033[1;32mRefresh complete.\033[0m Docs updated in %s\n", g_db_path);
    } else {
        fprintf(stderr, "\nRefresh completed with errors (exit code %d).\n", rc);
    }

    return rc;
}

