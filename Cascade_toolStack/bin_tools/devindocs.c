static int write_file(const char *path, const char *content) {
    FILE *f = fopen(path, "w");
    if (!f) {
        fprintf(stderr, "Error: cannot write to %s: %s\n", path, strerror(errno));
        return 1;
    }
    fputs(content, f);
    fclose(f);
    printf("\033[1;32mCreated:\033[0m %s\n", path);
    return 0;
}

static int file_exists(const char *path) {
    struct stat st;
    return stat(path, &st) == 0;
}

static int read_file(const char *path, char *buf, int buf_sz) {
    FILE *f = fopen(path, "r");
    if (!f) return -1;
    int n = fread(buf, 1, buf_sz - 1, f);
    buf[n] = 0;
    fclose(f);
    return n;
}

static int prompt_overwrite(const char *path) {
    if (!file_exists(path)) return 1;  /* OK to create */
    fprintf(stderr, "\033[1;33mFile exists:\033[0m %s\n", path);
    fprintf(stderr, "Overwrite? [y/N] ");
    char resp[16];
    if (!fgets(resp, sizeof(resp), stdin)) return 0;
    return (resp[0] == 'y' || resp[0] == 'Y');
}

static int ensure_dir(const char *path) {
    struct stat st;
    if (stat(path, &st) == 0) return 0;
    /* Create parent directories recursively */
    char tmp[1024];
    strncpy(tmp, path, sizeof(tmp) - 1);
    tmp[sizeof(tmp)-1] = 0;
    int len = strlen(tmp);
    for (int i = 1; i < len; i++) {
        if (tmp[i] == '/') {
            tmp[i] = 0;
            mkdir(tmp, 0755);
            tmp[i] = '/';
        }
    }
    return mkdir(path, 0755);
}

/* Merge a hook template into an existing hooks.v1.json file.
   Uses Python via popen() for proper JSON merging. */
static int merge_hook_into_file(const char *path, const char *hook_json, const char *hook_name) {
    char existing[65536] = "";
    int has_existing = (read_file(path, existing, sizeof(existing)) > 0);

    if (!has_existing) {
        /* No existing file — just write the template as-is */
        return write_file(path, hook_json);
    }

    /* Use Python to merge JSON properly */
    char tmp_py[512];
    snprintf(tmp_py, sizeof(tmp_py), "/tmp/devindocs_merge_%d.py", getpid());

    FILE *f = fopen(tmp_py, "w");
    if (!f) {
        fprintf(stderr, "Error: cannot write merge script\n");
        return write_file(path, hook_json);  /* fallback: overwrite */
    }

    fprintf(f,
"import sys, json\n"
"\n"
"existing_path = sys.argv[1]\n"
"hook_json_str = sys.argv[2]\n"
"hook_name = sys.argv[3]\n"
"\n"
"with open(existing_path, 'r') as f:\n"
"    existing = json.load(f)\n"
"\n"
"new_hook = json.loads(hook_json_str)\n"
"\n"
"# Merge: for each top-level key (PreToolUse, PostToolUse, etc.), append hooks\n"
"merged = existing\n"
"for event_type, hook_list in new_hook.items():\n"
"    if event_type in merged:\n"
"        # Check if this exact hook already exists (by command string)\n"
"        existing_cmds = set()\n"
"        for entry in merged[event_type]:\n"
"            for h in entry.get('hooks', []):\n"
"                existing_cmds.add(h.get('command', ''))\n"
"        for entry in hook_list:\n"
"            is_new = True\n"
"            for h in entry.get('hooks', []):\n"
"                if h.get('command', '') in existing_cmds:\n"
"                    is_new = False\n"
"                    break\n"
"            if is_new:\n"
"                # Try to merge into existing entry with same matcher\n"
"                matcher = entry.get('matcher', '')\n"
"                merged_in = False\n"
"                for e in merged[event_type]:\n"
"                    if e.get('matcher', '') == matcher:\n"
"                        e['hooks'].extend(entry['hooks'])\n"
"                        merged_in = True\n"
"                        break\n"
"                if not merged_in:\n"
"                    merged[event_type].append(entry)\n"
"    else:\n"
"        merged[event_type] = hook_list\n"
"\n"
"with open(existing_path, 'w') as f:\n"
"    json.dump(merged, f, indent=2)\n"
"    f.write('\\n')\n"
"\n"
"print(f'Merged hook into {existing_path}')\n"
);

    fclose(f);

    /* Write hook_json to a temp file to avoid shell escaping issues */
    char tmp_hook[512];
    snprintf(tmp_hook, sizeof(tmp_hook), "/tmp/devindocs_hook_%d.json", getpid());
    write_file(tmp_hook, hook_json);

    char cmd[2048];
    snprintf(cmd, sizeof(cmd), "python3 %s %s \"$(cat %s)\" %s 2>&1",
             tmp_py, path, tmp_hook, hook_name);

    int rc = system(cmd);

    unlink(tmp_py);
    unlink(tmp_hook);

    if (rc == 0) {
        printf("\033[1;32mMerged hook into:\033[0m %s\n", path);
    } else {
        fprintf(stderr, "Merge failed, falling back to overwrite.\n");
        return write_file(path, hook_json);
    }
    return 0;
}

/* Merge an MCP server into .devin/config.json's mcpServers section.
   Creates the file if it doesn't exist. */
static int merge_mcp_into_config(const char *config_path, const char *mcp_name,
                                  const char *mcp_json) {
    char existing[65536] = "";
    int has_existing = (read_file(config_path, existing, sizeof(existing)) > 0);

    if (!has_existing) {
        /* No config.json — create one with just this MCP server */
        char buf[70000];
        snprintf(buf, sizeof(buf),
            "{\n"
            "  \"mcpServers\": {\n"
            "    \"%s\": %s\n"
            "  }\n"
            "}\n", mcp_name, mcp_json);
        return write_file(config_path, buf);
    }

    /* Use Python to merge MCP server into existing config.json */
    char tmp_py[512];
    snprintf(tmp_py, sizeof(tmp_py), "/tmp/devindocs_mcp_merge_%d.py", getpid());

    FILE *f = fopen(tmp_py, "w");
    if (!f) {
        fprintf(stderr, "Error: cannot write merge script\n");
        return 1;
    }

    fprintf(f,
"import sys, json\n"
"\n"
"config_path = sys.argv[1]\n"
"mcp_name = sys.argv[2]\n"
"mcp_json_str = sys.argv[3]\n"
"\n"
"with open(config_path, 'r') as f:\n"
"    config = json.load(f)\n"
"\n"
"if 'mcpServers' not in config:\n"
"    config['mcpServers'] = {}\n"
"\n"
"mcp_config = json.loads(mcp_json_str)\n"
"config['mcpServers'][mcp_name] = mcp_config\n"
"\n"
"with open(config_path, 'w') as f:\n"
"    json.dump(config, f, indent=2)\n"
"    f.write('\\n')\n"
"\n"
"print(f'Added MCP server to {config_path}')\n"
);

    fclose(f);

    /* Write mcp_json to temp file */
    char tmp_mcp[512];
    snprintf(tmp_mcp, sizeof(tmp_mcp), "/tmp/devindocs_mcp_%d.json", getpid());
    write_file(tmp_mcp, mcp_json);

    char cmd[2048];
    snprintf(cmd, sizeof(cmd), "python3 %s %s %s \"$(cat %s)\" 2>&1",
             tmp_py, config_path, mcp_name, tmp_mcp);

    int rc = system(cmd);

    unlink(tmp_py);
    unlink(tmp_mcp);

    if (rc == 0) {
        printf("\033[1;32mAdded MCP server '%s' to:\033[0m %s\n", mcp_name, config_path);
    } else {
        fprintf(stderr, "Merge failed.\n");
    }
    return rc;
}

int cmd_gen(int argc, char **argv) {
    if (argc < 3) {
        fprintf(stderr, "Usage: devindocs gen <type> <name>\n");
        fprintf(stderr, "Types: rule, skill, workflow, hook, agents, mcp, config\n");
        fprintf(stderr, "Names: run 'devindocs templates' to see available templates\n");
        fprintf(stderr, "\nExamples:\n");
        fprintf(stderr, "  devindocs gen skill review\n");
        fprintf(stderr, "  devindocs gen mcp github\n");
        fprintf(stderr, "  devindocs gen config full\n");
        fprintf(stderr, "  devindocs gen hook block-destructive\n");
        return 1;
    }

    const char *type = argv[1];
    const char *name = argv[2];
    int force = (argc >= 4 && strcmp(argv[3], "--force") == 0);

    sqlite3 *db = db_open();
    sqlite3_stmt *st;

    sqlite3_prepare_v2(db,
        "SELECT content FROM templates WHERE type=? AND name=?",
        -1, &st, NULL);
    sqlite3_bind_text(st, 1, type, -1, SQLITE_STATIC);
    sqlite3_bind_text(st, 2, name, -1, SQLITE_STATIC);

    if (sqlite3_step(st) != SQLITE_ROW) {
        fprintf(stderr, "Template '%s/%s' not found.\n", type, name);
        fprintf(stderr, "Run 'devindocs templates' to see available templates.\n");
        sqlite3_finalize(st);
        sqlite3_close(db);
        return 1;
    }

    const char *content_raw = (const char*)sqlite3_column_text(st, 0);
    char *content = content_raw ? strdup(content_raw) : strdup("");
    sqlite3_finalize(st);

    /* Determine output path based on type */
    char path[1024];
    char cwd[1024];
    getcwd(cwd, sizeof(cwd));

    if (strcmp(type, "rule") == 0) {
        if (strcmp(name, "basic") == 0 || strcmp(name, "vbstyle") == 0 || strcmp(name, "cascade-handoff") == 0) {
            snprintf(path, sizeof(path), "%s/AGENTS.md", cwd);
        } else {
            snprintf(path, sizeof(path), "%s/.devin/rules/%s.md", cwd, name);
            char dir[1024];
            snprintf(dir, sizeof(dir), "%s/.devin/rules", cwd);
            ensure_dir(dir);
        }
        if (!force && !prompt_overwrite(path)) {
            fprintf(stderr, "Skipped.\n");
            free(content);
            sqlite3_close(db);
            return 0;
        }
        int rc = write_file(path, content);
        if (rc == 0) printf("\n\033[2mCascade will auto-load AGENTS.md from project root.\033[0m\n");
        sqlite3_close(db);
        free(content);
        return rc;

    } else if (strcmp(type, "skill") == 0) {
        snprintf(path, sizeof(path), "%s/.devin/skills/%s/SKILL.md", cwd, name);
        char dir[1024];
        snprintf(dir, sizeof(dir), "%s/.devin/skills/%s", cwd, name);
        ensure_dir(dir);
        if (!force && !prompt_overwrite(path)) {
            fprintf(stderr, "Skipped.\n");
            free(content);
            sqlite3_close(db);
            return 0;
        }
        int rc = write_file(path, content);
        if (rc == 0) printf("\n\033[2mInvoke with: /%s\033[0m\n", name);
        sqlite3_close(db);
        free(content);
        return rc;

    } else if (strcmp(type, "workflow") == 0) {
        snprintf(path, sizeof(path), "%s/.devin/workflows/%s.md", cwd, name);
        char dir[1024];
        snprintf(dir, sizeof(dir), "%s/.devin/workflows", cwd);
        ensure_dir(dir);
        if (!force && !prompt_overwrite(path)) {
            fprintf(stderr, "Skipped.\n");
            free(content);
            sqlite3_close(db);
            return 0;
        }
        int rc = write_file(path, content);
        sqlite3_close(db);
        free(content);
        return rc;

    } else if (strcmp(type, "hook") == 0) {
        /* Hooks merge into .devin/hooks.v1.json instead of overwriting */
        snprintf(path, sizeof(path), "%s/.devin/hooks.v1.json", cwd);
        char dir[1024];
        snprintf(dir, sizeof(dir), "%s/.devin", cwd);
        ensure_dir(dir);
        int rc = merge_hook_into_file(path, content, name);
        if (rc == 0) {
            printf("\n\033[2mHook merged into .devin/hooks.v1.json — existing hooks preserved.\033[0m\n");
        }
        sqlite3_close(db);
        free(content);
        return rc;

    } else if (strcmp(type, "agents") == 0) {
        snprintf(path, sizeof(path), "%s/.devin/agents/%s/AGENT.md", cwd, name);
        char dir[1024];
        snprintf(dir, sizeof(dir), "%s/.devin/agents/%s", cwd, name);
        ensure_dir(dir);
        if (!force && !prompt_overwrite(path)) {
            fprintf(stderr, "Skipped.\n");
            free(content);
            sqlite3_close(db);
            return 0;
        }
        int rc = write_file(path, content);
        if (rc == 0) printf("\n\033[2mSubagent '%s' available via run_subagent.\033[0m\n", name);
        sqlite3_close(db);
        free(content);
        return rc;

    } else if (strcmp(type, "mcp") == 0) {
        /* MCP servers merge into .devin/config.json's mcpServers section */
        snprintf(path, sizeof(path), "%s/.devin/config.json", cwd);
        char dir[1024];
        snprintf(dir, sizeof(dir), "%s/.devin", cwd);
        ensure_dir(dir);
        int rc = merge_mcp_into_config(path, name, content);
        if (rc == 0) {
            printf("\n\033[2mMCP server added to .devin/config.json — restart Devin CLI to load.\033[0m\n");
            printf("\033[2mAdd permission: \\\"mcp__%s__*\\\" to config.json permissions.allow\033[0m\n", name);
        }
        sqlite3_close(db);
        free(content);
        return rc;

    } else if (strcmp(type, "config") == 0) {
        /* Config templates write a full .devin/config.json */
        snprintf(path, sizeof(path), "%s/.devin/config.json", cwd);
        char dir[1024];
        snprintf(dir, sizeof(dir), "%s/.devin", cwd);
        ensure_dir(dir);
        if (!force && !prompt_overwrite(path)) {
            fprintf(stderr, "Skipped.\n");
            free(content);
            sqlite3_close(db);
            return 0;
        }
        int rc = write_file(path, content);
        if (rc == 0) printf("\n\033[2mConfig written to .devin/config.json — restart Devin CLI to load.\033[0m\n");
        sqlite3_close(db);
        free(content);
        return rc;

    } else {
        fprintf(stderr, "Unknown type: %s\n", type);
        fprintf(stderr, "Valid types: rule, skill, workflow, hook, agents, mcp, config\n");
        sqlite3_close(db);
        free(content);
        return 1;
    }
}

